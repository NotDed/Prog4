public class Opes {
    public static int suma(int n, int k){
        return n+k;
    }
    
    public static int resta(int n, int k){
        return n-k;
    }
    
    public static int multi(int n, int k){
        return n*k;
    }
    
    public static int div(int n, int k){
        return n/k;
    }
    
    public static int mod(int n, int k){
        return n%k;
    }
    
}
